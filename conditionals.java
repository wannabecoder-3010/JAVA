public class conditionals {
    public static void main(String[] args) {
        
        String fruit = "apple";

        switch(fruit){
            case "apple":
                 System.out.println("it's an apple");
                 break;
            case "banana":
                 System.out.println("it's a banana");
                 break;
            case "litchi":
                 System.out.println("it's a litchi");
                 break;
            default:
                 System.out.println("invalid fruit");         

            }
        }

    }

    

